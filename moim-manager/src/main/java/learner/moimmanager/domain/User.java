package learner.moimmanager.domain;

public class User {

    private String name;
    private String nickName;
    private String password;
    private String email;

    public User() {
    }

    public String getName() {
        return name;
    }

    public String getNickName() {
        return nickName;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }
}
